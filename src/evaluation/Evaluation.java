package evaluation;

public class Evaluation {
	private int AcademicNumber;
	private String ID;
	private int kindness;
	private int sincerity;
	private int curriculum;
	private int interest;
	private int delivery;
	private String coment;
	private String evaluation_date;
	public int getAcademicNumber() {
		return AcademicNumber;
	}
	public void setAcademicNumber(int academicNumber) {
		AcademicNumber = academicNumber;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public int getKindness() {
		return kindness;
	}
	public void setKindness(int kindness) {
		this.kindness = kindness;
	}
	public int getSincerity() {
		return sincerity;
	}
	public void setSincerity(int sincerity) {
		this.sincerity = sincerity;
	}
	public int getCurriculum() {
		return curriculum;
	}
	public void setCurriculum(int curriculum) {
		this.curriculum = curriculum;
	}
	public int getInterest() {
		return interest;
	}
	public void setInterest(int interest) {
		this.interest = interest;
	}
	public int getDelivery() {
		return delivery;
	}
	public void setDelivery(int delivery) {
		this.delivery = delivery;
	}
	public String getComent() {
		return coment;
	}
	public void setComent(String coment) {
		this.coment = coment;
	}
	public String getEvaluation_date() {
		return evaluation_date;
	}
	public void setEvaluation_date(String evaluation_date) {
		this.evaluation_date = evaluation_date;
	}
	
	
}
